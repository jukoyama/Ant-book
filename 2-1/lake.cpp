#include <bits/stdc++.h>
using namespace std;

int n = 10;
int m = 12;
vector<vector<char>> l = {{'W','.','.','.','.','.','.','.','.','W','W','.'},
			  {'.','W','W','W','.','.','.','.','.','W','W','W'},
			  {'.','.','.','.','W','W','.','.','.','W','W','.'},
			  {'.','.','.','.','.','.','.','.','.','W','W','.'},
			  {'.','.','.','.','.','.','.','.','.','W','.','.'},
			  {'.','.','W','.','.','.','.','.','.','W','.','.'},
			  {'.','W','.','W','.','.','.','.','.','W','W','.'},
			  {'W','.','W','.','W','.','.','.','.','.','W','.'},
			  {'.','W','.','W','.','.','.','.','.','.','W','.'},
			  {'.','.','W','.','.','.','.','.','.','.','W','.'},
};

void dfs (int x, int y) {
  l.at(x).at(y) = '.';

  for (int dx=-1; dx<=1; dx++) {
    for (int dy=-1; dy<=1; dy++) {
      int nx = x + dx;
      int ny = y + dy;
      if ( nx >= 0 && ny >= 0 && nx < n && ny < m && l.at(nx).at(ny) == 'W') {
	dfs(nx, ny);
      }
    }
  }
  return ;
}

int solve () {
  int ans = 0;
  for (int i=0; i<n; i++) {
    for (int j=0; j<m; j++) {
      if (l.at(i).at(j) == 'W') {
	dfs(i, j);
	ans ++;
      }
    }
  }
  return ans;
}

int main() {
  int ans = solve();
  cout << ans << "\n";
}
